    public void execute(){
         try{
            ServerSocket serverSocket = new ServerSocket(port);
 
            changeText("Chat Server is listening on port " + port);
            while (true) {
                Socket socket = serverSocket.accept();
                changeText("New user connected");
 
                ClientHandler newUser = new ClientHandler(socket);
                new Thread(newUser).start();
 
            }
 
        } catch (IOException ex) {
            changeText("Error in the server: " + ex.getMessage());
            ex.printStackTrace();
        }
        
    }
