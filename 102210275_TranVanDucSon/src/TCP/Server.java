    public String decimalToBinary(int decimal) {
            if (decimal == 0) {
                return "0";
            }
            StringBuilder binary = new StringBuilder();
            while (decimal > 0) {
                int remainder = decimal % 2;
                binary.insert(0, remainder);
                decimal /= 2;
            }
            
            String str = String.format("%8s", binary.toString()).replace(' ', '0');

            return str;
        }
        
    public boolean checkSym(int decimal){
        String str = decimalToBinary(decimal);
        changeText("Binary number: " + str);
        int length = str.length();

        for (int i = 0; i < length / 2; i++) {
            if (str.charAt(i) != str.charAt(length - i - 1)) {
                return false;
            }
        }

        return true;
    }
    
    class ClientHandler extends Thread{
        
        private Socket socket;
        
        public ClientHandler(Socket sk){
            this.socket = sk;
        }
        @Override
        public void run(){
            try {
                DataOutputStream out = new DataOutputStream(socket.getOutputStream());
                DataInputStream in = new DataInputStream(socket.getInputStream());
        
                while(true){
                    //Nhận dữ liệu từ client
                    String clientChat = in.readUTF();                             
                    if(clientChat.equals("bye")){
                        socket.close();
                        break;
                    }                   
                    boolean res= checkSym(Integer.parseInt(clientChat.trim()));
                    String bi = decimalToBinary(Integer.parseInt(clientChat.trim()));
                    if(res){
                        out.writeUTF(bi +" is the right number. Stop button function.");
                        out.writeUTF("1");
                        socket.close();
                    }
                    else{
                        out.writeUTF(bi +" is not qualified");
                        out.writeUTF("0");
                    }
                }
            } catch (Exception e) {
                changeText(e.getMessage());
                e.printStackTrace();
            }
        }


    }
    

    

